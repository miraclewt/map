function [tileNames, latlimS, latlimN, lonlimW, lonlimE] = gtopo30tiles
%GTOPO30TILES GTOPO30 data tilenames and quadrangle limits
%
%   [tileNames, latlimS, latlimN, lonlimW, lonlimE] = gtopo30tiles()
%   returns a cell array of strings denoting the names of the GTOPO30 tiles
%   in tileNames, the latitude bounding box coordinates in the double
%   arrays latlimS (southern limits) and latlimN (northern limits), and the
%   longitude bounding box coordinates in the double arrays, lonlimW
%   (western limits) and lonlimE (eastern limits).
%
%   See also DEMDATAUI, GTOPO30, GTOPO30S.

% Copyright 2011 The MathWorks, Inc.

% Define names and bounding rectangle limits of the tiles.
tiles = { ...
    'w180n90' 40   90   -180  -140; ...
    'w140n90' 40   90   -140  -100; ...
    'w100n90' 40   90   -100   -60; ...
    'w060n90' 40   90    -60   -20; ...
    'w020n90' 40   90    -20    20; ...
    'e020n90' 40   90     20    60; ...
    'e060n90' 40   90     60   100; ...
    'e100n90' 40   90    100   140; ...
    'e140n90' 40   90    140   180; ...
    'w180n40' -10   40   -180  -140; ...
    'w140n40' -10   40   -140  -100; ...
    'w100n40' -10   40   -100   -60; ...
    'w060n40' -10   40    -60   -20; ...
    'w020n40' -10   40    -20    20; ...
    'e020n40' -10   40     20    60; ...
    'e060n40' -10   40     60   100; ...
    'e100n40' -10   40    100   140; ...
    'e140n40' -10   40    140   180; ...
    'w180s10' -60  -10   -180  -140; ...
    'w140s10' -60  -10   -140  -100; ...
    'w100s10' -60  -10   -100   -60; ...
    'w060s10' -60  -10    -60   -20; ...
    'w020s10' -60  -10    -20    20; ...
    'e020s10' -60  -10     20    60; ...
    'e060s10' -60  -10     60   100; ...
    'e100s10' -60  -10    100   140; ...
    'e140s10' -60  -10    140   180; ...
    'w180s60' -90  -60   -180  -120; ...
    'w120s60' -90  -60   -120   -60; ...
    'w060s60' -90  -60    -60     0; ...
    'w000s60' -90  -60      0    60; ...
    'e060s60' -90  -60     60   120; ...
    'e120s60' -90  -60    120   180; ...
    };

tileNames = tiles(1:end,1);
latlimS  = [tiles{1:end,2}]';
latlimN  = [tiles{1:end,3}]';
lonlimW  = [tiles{1:end,4}]';
lonlimE  = [tiles{1:end,5}]';
