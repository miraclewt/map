function [tileNames, latlimS, latlimN, lonlimW, lonlimE, rTiles, cTiles] ...
    = globetiles
%GLOBETILES GLOBE data tilenames and quadrangle limits
%
%   [tileNames, latlimS, latlimN, lonlimW, lonlimE, rTile, cTile] =
%   globetiles() returns a cell array of strings denoting the names of the
%   GLOBE DEM tiles in tileNames, the latitude bounding box coordinates in
%   the double arrays latlimS (southern limits) and latlimN (northern
%   limits), the longitude bounding box coordinates in the double arrays,
%   lonlimW (western limits) and lonlimE (eastern limits), and the row and
%   column tile numbers for each GLOBEDEM map, in the double arrays rTile
%   and cTile.
%
%   See also DEMDATAUI, GLOBEDEM, GLOBEDEMS.

% Copyright 2011 The MathWorks, Inc.

% Define names and bounding rectangle limits of the tiles.
tiles = { ...
    'a10g'   50   90 -180  -90    1    1; ...
    'b10g'   50   90  -90    0    1    2; ...
    'c10g'   50   90    0   90    1    3; ...
    'd10g'   50   90   90  180    1    4; ...
    'e10g'    0   50 -180  -90    2    1; ...
    'f10g'    0   50  -90    0    2    2; ...
    'g10g'    0   50    0   90    2    3; ...
    'h10g'    0   50   90  180    2    4; ...
    'i10g'  -50    0 -180  -90    3    1; ...
    'j10g'  -50    0  -90    0    3    2; ...
    'k10g'  -50    0    0   90    3    3; ...
    'l10g'  -50    0   90  180    3    4; ...
    'm10g'  -90  -50 -180  -90    4    1; ...
    'n10g'  -90  -50  -90    0    4    2; ...
    'o10g'  -90  -50    0   90    4    3; ...
    'p10g'  -90  -50   90  180    4    4; ...   
    };

tileNames = tiles(1:end,1);
latlimS  = [tiles{1:end,2}]';
latlimN  = [tiles{1:end,3}]';
lonlimW  = [tiles{1:end,4}]';
lonlimE  = [tiles{1:end,5}]';
rTiles   = [tiles{1:end,6}]';
cTiles   = [tiles{1:end,7}]';